// Lấy tin tức theo ID
export const getNewsById = (id) => {
  return newsData.find(news => news.id === parseInt(id));
};

// Lấy tin tức liên quan (cùng category, trừ tin hiện tại)
export const getRelatedNews = (currentId, category) => {
  return newsData
    .filter(news => news.id !== currentId && news.category === category)
    .slice(0, 3); // Chỉ lấy 3 tin liên quan
};
